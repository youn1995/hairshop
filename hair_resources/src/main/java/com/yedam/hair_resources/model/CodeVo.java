package com.yedam.hair_resources.model;

public class CodeVo {
	private String code_no;
	private String primary_code;
	private String code_name;
	private String secondary_code;
	private String code_info;
	public String getCode_no() {
		return code_no;
	}
	public void setCode_no(String code_no) {
		this.code_no = code_no;
	}
	public String getPrimary_code() {
		return primary_code;
	}
	public void setPrimary_code(String primary_code) {
		this.primary_code = primary_code;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getSecondary_code() {
		return secondary_code;
	}
	public void setSecondary_code(String secondary_code) {
		this.secondary_code = secondary_code;
	}
	public String getCode_info() {
		return code_info;
	}
	public void setCode_info(String code_info) {
		this.code_info = code_info;
	}
	
	
	
}
